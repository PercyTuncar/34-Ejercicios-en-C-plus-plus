// La función ceil() redondea un número hacia arriba.
// Crear una aplicación que redondea un número hacia arriba.
#include <iostream>
#include <cmath>
using namespace std;
int main() {
    float num;
    cout << "Ingrese un numero: ";
    cin >> num;
    cout << "El numero redondeado es: " << ceil(num) << endl;
    return 0;
}

