// La funcion sqrt() devuelve la raiz cuadrada de un número.
// Crear una aplicación que calcula la raiz cuadrada de un número, usar la función sqrt() para calcularla.
#include <iostream>
#include <cmath>
using namespace std;
int main(){
    float numero;
    cout << "Ingrese un numero: ";
    cin >> numero;
    cout << "La raiz cuadrada del numero es: " << sqrt(numero) << endl;
    return 0;
}

